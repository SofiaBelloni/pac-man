package view;

/**
 * This enumeration represents the scene that can be loaded.
 */
public enum GameScene {
    /**
     * The Main Menu scene.
     */
    MAINMENU,
    /**
     * The game scene.
     */
    GAME,
    /**
     * The game over scene.
     */
    GAMEOVER,
    /**
     * The scene with the leader board.
     */
    SCORE,
    /**
     * The scene with the settings of the game.
     */
    SETTINGS,
    /**
     * The scene with the instruction of the game.
     */
    INSTRUCTIONS;

}
